# Exercise 10: What was that?


# Exercise example
tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."

# Using ''' instead of """ will not register it as a
# multi-line string.
fat_cat = """
I'll do a list:
\t* Cat food
\t* fishies
\t* Catnip\n\t* Grass
"""

puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat

