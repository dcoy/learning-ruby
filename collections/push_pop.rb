# Push and Pop elements to an array
teams = ['Spurs', 'Mavericks', 'Bulls', 'Knicks']

# Add a value to the end of an array - push
teams.push('Raptors')
puts "Array after pushing item to array: #{teams}"

# Add a value to the end of an array - pop
teams.pop()
puts "Array after popping item off array: #{teams}"
